DROP TABLE user_roles IF EXISTS;
DROP TABLE meals_dishes IF EXISTS;
DROP TABLE votes IF EXISTS;
DROP TABLE dishes IF EXISTS;
DROP TABLE meals IF EXISTS;
DROP TABLE restaurants IF EXISTS;
DROP TABLE users IF EXISTS;
DROP SEQUENCE global_seq IF EXISTS;

CREATE SEQUENCE GLOBAL_SEQ AS INTEGER START WITH 1000;

CREATE TABLE users
(
    id                INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ PRIMARY KEY,
    name              VARCHAR(255)            NOT NULL,
    password          VARCHAR(255)            NOT NULL
);
CREATE UNIQUE INDEX users_unique_idx ON users (name);

CREATE TABLE restaurants
(
    id              INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ PRIMARY KEY,
    name            VARCHAR(255) NOT NULL,
    creator_user_id INTEGER      NOT NULL,
    CONSTRAINT restaurants_unique_idx UNIQUE (name),
    FOREIGN KEY (creator_user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE meals
(
    id            INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ PRIMARY KEY,
    date          TIMESTAMP NOT NULL,
    restaurant_id INTEGER   NOT NULL,
    CONSTRAINT meals_unique_idx UNIQUE (restaurant_id, date),
    foreign key (restaurant_id) references restaurants (id) ON DELETE CASCADE
);

CREATE TABLE dishes
(
    id    INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ PRIMARY KEY,
    name  VARCHAR(255) NOT NULL,
    price INT          NOT NULL,
    CONSTRAINT dishes_unique_idx UNIQUE (name, price)
);

create table meals_dishes
(
    meal_id INTEGER NOT NULL,
    dish_id INTEGER NOT NULL,
    CONSTRAINT meals_dishes_unique_idx UNIQUE (meal_id, dish_id),
    FOREIGN KEY (meal_id) REFERENCES meals (id) ON DELETE CASCADE,
    FOREIGN KEY (dish_id) REFERENCES dishes (id) ON DELETE CASCADE
);

create table user_roles
(
    user_id INTEGER      NOT NULL,
    role    VARCHAR(255) NOT NULL,
    CONSTRAINT user_roles_unique_idx UNIQUE (user_id, role),
    FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE votes
(
    id    INTEGER GENERATED BY DEFAULT AS SEQUENCE GLOBAL_SEQ PRIMARY KEY,
    user_id INTEGER NOT NULL,
    meal_id INTEGER NOT NULL,
    CONSTRAINT user_meal_unique_idx UNIQUE (user_id, meal_id),
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY (meal_id) REFERENCES meals(id) ON DELETE CASCADE
);
